#10. Faça um Programa que escreva “Primeira aula de Python foi TOP” se tempo for menor que 4 horas utilizando atribuição condicional, demonstrando as duas formas.



tempo = float(input('Quantas horas a primeira aula teve? \n'))

if tempo < 4:
  print('Primeira aula de Python foi TOP.')
else:
     print('Primeira aula de Python durou ', tempo, 'horas.')

